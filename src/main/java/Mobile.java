
public class Mobile {
	public String name;
	public String color;
	public String brand;
	
	public Mobile(String name, String color, String brand) {
		this.name = name;
		this.color = color;
		this.brand = brand;
	}
	
	
	public void printMobile() {
		System.out.println(name);
		System.out.println(color);
		System.out.println(brand);
		System.out.println();
	}
	
	
	
	
}
